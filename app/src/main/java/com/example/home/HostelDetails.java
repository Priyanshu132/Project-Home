package com.example.home;

public class HostelDetails {

    private String Hostel_name;
    private String Address_of_hostel;
    private String Landline_of_hostel;


    public  HostelDetails(){

    }

    public String getHostel_name() {
        return Hostel_name;
    }

    public void setHostel_name(String hostel_name) {
        Hostel_name = hostel_name;
    }

    public String getAddress_of_hostel() {
        return Address_of_hostel;
    }

    public void setAddress_of_hostel(String address_of_hostel) {
        Address_of_hostel = address_of_hostel;
    }

    public String getLandline_of_hostel() {
        return Landline_of_hostel;
    }

    public void setLandline_of_hostel(String landline_of_hostel) {
        Landline_of_hostel = landline_of_hostel;
    }
}
